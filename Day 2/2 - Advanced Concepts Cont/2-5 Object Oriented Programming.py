# Extract from Python Tutorial. 9. Classes https://docs.python.org/2/tutorial/classes.html

# “Compared with other programming languages, Python’s class mechanism adds classes with a
# minimum of new syntax and semantics. It is a mixture of the class mechanisms found in C++
# and Modula-3. Python classes provide all the standard features of Object Oriented Programming:
# the class inheritance mechanism allows multiple base classes, a derived class can override any
# methods of its base class or classes, and a method can call the method of a base class with the
# same name. Objects can contain arbitrary amounts and kinds of data. As is true for modules, classes
# partake of the dynamic nature of Python: they are created at runtime, and can be modified further
# after creation.”

